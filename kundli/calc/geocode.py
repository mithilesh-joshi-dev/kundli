def lookup_city(name: str) -> tuple[float, float, float] | None:
    """Look up city coordinates. Returns (lat, lon, utc_offset) or None."""
    key = name.strip().lower()
    return CITIES.get(key)


def fuzzy_search(name: str) -> list[tuple[str, float, float]]:
    """Search for cities matching a partial name."""
    key = name.strip().lower()
    matches = []
    for city, (lat, lon, _) in CITIES.items():
        if key in city:
            matches.append((city.title(), lat, lon))
    return matches[:10]
